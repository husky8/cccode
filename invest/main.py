def getMincost(havingList):
    minHistory={"cost":99999}
    for historyDay in havingList:
        if historyDay["cost"]< minHistory["cost"] and historyDay[ "merge"] is not True:
            minHistory=historyDay

    return minHistory

def averageCost(today,havingList):
    for historyDay in havingList:
        # print(historyDay)
        if today[1]/historyDay["cost"] <0.92 and historyDay["merge"] is not True:
            minHistory=getMincost(havingList)

            # print(historyDay)
            # print(minHistory)
            if minHistory== {'cost': 99999}:
                continue
            if historyDay != minHistory:
                havingList.append({"date": "{}与{}合并".format(historyDay["date"],minHistory["date"]),
                                   "cost": 20000/(historyDay["much"]+minHistory["much"]),
                                   "much":historyDay["much"]+minHistory["much"],
                                   "merge": True,
                                   "expect": 1
                                   })

                havingList.remove(historyDay)
                havingList.remove(minHistory)

    return (havingList)

def decisions(today,havingList):
    """决策"""
    havingList=averageCost(today,havingList) #先平均成本

    saleList = []
    for historyDay in havingList:
        historyDay["expect"]=max(1,historyDay["expect"]*0.9993)
        if today[1]>=historyDay["cost"]*historyDay["expect"]:
            saleList.append({
                "buy":historyDay["date"],
                "sale":today[0],
                "income":round((today[2]-historyDay["cost"])*historyDay["much"],2)})
            havingList.remove(historyDay)
    return havingList,saleList


